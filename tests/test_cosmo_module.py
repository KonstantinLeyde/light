def test_luminosity_distance_jacobian(cosmology_object):
    z_test = jnp.linspace(1e-3, 1, 10001)
    x_boundaries = cosmology_object.get_luminosity_distance_from_z(z_test)

    z_centers, delta_z = jax_utils.compute_centers_and_delta_from_array(z_test)

    jacobian_numerical = (x_boundaries[1:] - x_boundaries[:-1]) / delta_z
    jacobian_module = cosmology_object.get_dluminosity_distance_over_dz_from_z(
        z=z_centers
    )

    print(jnp.max(abs(jacobian_numerical - jacobian_module)))
    print(
        jnp.max(
            abs(jacobian_numerical - jacobian_module)
            / (jacobian_numerical + jacobian_module)
        )
    )

    print(jacobian_numerical, jacobian_module)

    assert jnp.allclose(jacobian_numerical, jacobian_module, rtol=1e-4, atol=20)


